    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(Validacao())
        {
            Editora editora = new Editora();
            editora.setNome(camponome.getText());
            editora.setEndereco(campoendereco.getText());
            editora.setTelefone(campotelefone.getText());
            editora.setCnpj(campocnpj.getText());
            editora.setEmail(campoemail.getText());


            DAOEditora atualizar = new DAOEditora();
            if(atualizar.alteraEditora(editora))
            {
                JOptionPane.showMessageDialog(null, "Registro Atualizado com Sucesso.");
                dispose();
                new EditarEditoraView().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Erro ao Atualizar registro");
                dispose();
                new EditarEditoraView().setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

