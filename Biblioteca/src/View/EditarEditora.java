    private boolean Validacao() {
        if (camponome.getText().equals("") || campoendereco.getText().equals("") || campotelefone.getText().equals("") || campocnpj.getText().equals("") || campoemail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.", "Erro", 0);
            return false;
        }
        else if (campocnpj.getText().length()!=14) {
            JOptionPane.showMessageDialog(null, "Preencha o CNPJ corretamente.", "Erro", 0);
            campocnpj.setText("");
            return false;
        }
        return true;
    }
    
