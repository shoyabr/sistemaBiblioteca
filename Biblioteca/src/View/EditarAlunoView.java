    private void tabelaDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaDadosMouseClicked
         if(Valida())
        {
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente atualizar esse registro?");
            if(resposta == 0)
            {
                Aluno aluno = new Aluno();
                int linha = tabelaDados.getSelectedRow();
                aluno .setNome(tabelaDados.getValueAt(linha, 0).toString());
                aluno .setCpf(tabelaDados.getValueAt(linha, 1).toString());
                aluno .setEndereco(tabelaDados.getValueAt(linha, 2).toString());
                aluno .setTelefone(tabelaDados.getValueAt(linha, 3).toString());
                new EditarAluno(aluno).setVisible(true);
                dispose();
            }
        }
    }//GEN-LAST:event_tabelaDadosMouseClicked

