    public ExcluirEditora() {
        initComponents();
        DAOEditora dadosEditora = new DAOEditora();
        ArrayList<Editora> listaEditoras = new ArrayList();
        listaEditoras = dadosEditora.selecionarTodosRegistros();
        //criando um modelo para a JTable
        DefaultTableModel modelo = (DefaultTableModel) tabelaDados.getModel();
        for(Editora editora : listaEditoras)
        {
            Object[] dados = {editora.getNome() ,editora.getEndereco() ,editora.getTelefone() ,editora.getCnpj() ,editora.getEmail()};
            modelo.addRow(dados);
        }
    }

    
   public boolean Valida()
    {
        if(tabelaDados.getSelectedRow()!=-1)
        {
            return true;
        }
        return false;
    }
    
