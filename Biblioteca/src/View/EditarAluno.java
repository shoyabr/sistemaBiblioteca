    private boolean Validacao() {
        if (camponome.getText().equals("") || campocpf.getText().equals("") || campoendereco.getText().equals("") || campotelefone.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.", "Erro", 0);
            return false;
        }

        else if(campocpf.getText().length()!=11)
        {
            JOptionPane.showMessageDialog(null, "Preencha um cpf valido.", "Erro", 0);
            campocpf.setText("");
            return false;
        }
        return true;
    }
    
