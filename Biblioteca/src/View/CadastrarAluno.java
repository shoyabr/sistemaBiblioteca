    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(Validacao())
        {
            Aluno aluno = new Aluno();
            aluno.setNome(camponome.getText());
            aluno.setCpf(campocpf.getText());
            aluno.setEndereco(campoendereco.getText());
            aluno.setTelefone(campotelefone.getText());

            DAOAluno insereDados = new DAOAluno();
            if(insereDados.insereAluno(aluno))
            {
                JOptionPane.showMessageDialog(null, "Registro cadastrado com sucesso!");
                limparCampos();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar registro, tente novamente.");
                limparCampos();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

