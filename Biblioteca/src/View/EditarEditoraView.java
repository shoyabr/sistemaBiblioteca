    private void tabelaDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaDadosMouseClicked
        if(Valida())
        {
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente atualizar esse registro?");
            if(resposta == 0)
            {
                Editora editora = new Editora();
                int linha = tabelaDados.getSelectedRow();
                editora .setNome(tabelaDados.getValueAt(linha, 0).toString());
                editora .setEndereco(tabelaDados.getValueAt(linha, 1).toString());
                editora .setTelefone(tabelaDados.getValueAt(linha, 2).toString());
                editora .setCnpj(tabelaDados.getValueAt(linha, 3).toString());
                editora .setEmail(tabelaDados.getValueAt(linha, 4).toString());
                new EditarEditora(editora).setVisible(true);
                dispose();
            }
        }
    }//GEN-LAST:event_tabelaDadosMouseClicked

